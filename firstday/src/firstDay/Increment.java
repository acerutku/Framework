package firstDay;

public class Increment {
    public static void main(String[] args){

        int sayi1 = 12 ;
        System.out.println(sayi1);  //12
        sayi1 = sayi1 + 3; // increment ===> birinci yol uzun olan
        System.out.println(sayi1); //15

        int sayi2= 20;
        System.out.println(sayi2);//20
        sayi2 = sayi2 + 7; //increment birinci yol uzun
        System.out.println(sayi2);


    }
}

